/**
 * Mutable（ミュータブル）
 * 一度作成したらその値自体を変更できない特性。
 * 例えば JavaScript では文字列や数値は Mutable です。
 *
 * Immutable (イミュータブル)
 * 一度作成した後もその値自体を変更できる特性。
 * 例えば JavaScript では配列やオブジェクトは Immutable です。
 */

